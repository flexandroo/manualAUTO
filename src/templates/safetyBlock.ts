// Канонічний розділ "Основні положення" — джерело: офіційний шаблон Termojet.
// Експортує готові масиви PageElement[] для двох сторінок безпеки так, щоб
// "Обережно", "Увага" та "Забороняється" були окремими warning-елементами
// (з відповідним рівнем), а пункти "Перекачувані рідини" / "Інструкції з
// безпеки" — окремими bulletList-елементами, а не злитими в тіло
// підрозділу. initialData.ts підставляє їх у дві стандартні сторінки.

import type { PageElement } from '../types/instruction';
import { newId } from '../utils/id';

export const SAFETY_BLOCK_TITLE = 'Основні положення';

function sub(number: string, heading: string, body: string): PageElement {
  return {
    id: newId('s'),
    type: 'subsection',
    number,
    heading,
    body,
  };
}

function bullets(items: string[]): PageElement {
  return { id: newId('bl'), type: 'bulletList', items };
}

function warn(
  level: 'info' | 'warning' | 'danger',
  title: string,
  body: string
): PageElement {
  return { id: newId('w'), type: 'warning', level, title, body };
}

